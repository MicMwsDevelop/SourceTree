SELECT D.[f受注番号]
      ,D.[f年度]

      ,H.[f受注日]
			,H.[f受注承認日]
			,H.[f売上承認日]
      ,H.[f納期]
		,H.[f販売種別]
		,H.[f販売先コード]
      ,H.[f販売先]
      ,H.[fユーザーコード]
      ,H.[fユーザー]
      ,[f商品コード]
      ,[f商品名]
			,H.[f受注金額]
      ,H.[fSV利用開始年月]
      ,H.[fSV利用終了年月]
      --,H.[f件名]
			,H.[fBshCode3]
			,H.[f担当支店名]
			,H.[f担当者コード]
			,H.[f担当者名]
 

      --,[f表示順]
      --,[f区分]
      --,[f区分名]
			--,H.[f備考]
      --,[f数量]
      --,[f標準価格]
      --,[f金額]
      --,[f提供価格]
      --,[f税区分]
      --,[f税率]
      --,[f税込区分]
      --,[f売上原価]
      --,[f掛率]
      --,[f商品区分1]
      --,[f商品区分2]
  FROM [JunpDB].[dbo].[tMih受注詳細] AS D
  LEFT JOIN [JunpDB].[dbo].[tMih受注ヘッダ] AS H ON D.[f受注番号] = H.[f受注番号]

  where D.[f商品コード] = '800121' or D.[f商品コード] = '800161' or D.[f商品コード] = '800162'
  order by H.[fユーザーコード], D.[f受注番号], D.[f表示順]